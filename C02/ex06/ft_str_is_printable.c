int	ft_str_is_printable(char *str)
{
	int	i;
	int	pr;

	i = 0;
	pr = 1;
	while (str[i] != '\0')
	{
		if (!(str[i] >= ' ') || str[i] == 127)
			pr = 0;
		i++;
	}
	return (pr);
}
